def gen_symmetric_Rs(n):
    """ This function returns a list of all symmetric matrices R of size n x n. 
    They are in one-to-one correspondence with the elements of the diagonal Clifford group
    on n qubits in the quocient space Clifford(n)/Paulis(n).
    
    Args:
        n (int): size of the returned squared matrices
    
    Returns:
        list of (int) numpy arrays of shape (n,n) """
    
    Rs=[]
    bs=list(itertools.product([0,1], repeat=n)) # list of possible bitstrings of length n
    os=list(itertools.product([0,1], repeat=(sum([n-1-i for i in range(n-1)])))) # same for upper off-diagonal number of elements
    for b in bs:
        for o in os:
            R=np.zeros((n,n),dtype=np.int8)
            for i in range(n):
                R[i,i]=b[i] # a 1 here corresponds to a phase gate on qubit i
                for j in range(n-1-i):
                    if o[sum([n-k-1 for k in range(i)])+j]==1:
                        R[i,i+j+1]=1 # a 1 here corresponds to a CZ gate between the indexed qubits
                        R[i+j+1,i]=1
            Rs.append(R.copy())
    return Rs

def gen_symmetric_Rs_for_CZS(n):
    """ This function returns a list of all symmetric matrices R of size n x n with zeros on the diagonal. 
    They are in one-to-one correspondence with the elements of the diagonal Clifford group
    on n qubits in the quocient space Clifford(n)/Paulis(n) that are generated by CZ gates only.
    
    Args:
        n (int): size of the returned squared matrices
    
    Returns:
        list of (int) numpy arrays of shape (n,n) """
    
    Rs=[]
    os=list(itertools.product([0,1], repeat=(sum([n-1-i for i in range(n-1)]))))
    for o in os:
        R=np.zeros((n,n),dtype=np.int8)
        for i in range(n):
            for j in range(n-1-i):
                if o[sum([n-k-1 for k in range(i)])+j]==1:
                    R[i,i+j+1]=1
                    R[i+j+1,i]=1
        Rs.append(R.copy())
    return Rs

def diagonal_Clifford_from_R(R):
    """ This function returns the diagonal Clifford operator corresponding to a given symmetric matrix R.
    It returns the operator as a numpy 1d-array, i.e. only the diagonal elements are stored.
    
    Args:
        R (int) numpy array of shape (n,n): symmetric matrix defining the Clifford operator
    
    Returns:
        (complex) numpy 1d-array of length 2**n """
    
    n=R.shape[0]
    out=np.zeros(2**n, dtype=np.complex64)
    vs=list(itertools.product([0,1], repeat=n))
    for v in vs:
        index=0
        for i in range(n):
            index+=v[i]*2**(n-1-i)
        out[index]=1j**(np.array(v)@R@np.array(v))
    return out

def diagonal_paulis(n):
    """ This function returns a list of all n-qubit Pauli Z operators.

    Args:
        n (int): number of qubits the operators act on

    Returns:
        list of (int) numpy 1d-arrays of length 2**n """
    
    diag_p=[]
    Z=np.array([1,-1], dtype=np.int8)
    Id=np.array([1,1], dtype=np.int8)
    bs=list(itertools.product([0,1], repeat=n))
    for b in bs:
        out=np.array(1, dtype=np.int8)
        for i in range(n):
            if b[i]==1:
                out=np.kron(out,Z)
            else:
                out=np.kron(out,Id)
        diag_p.append(out.copy())
    return diag_p

