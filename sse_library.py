def tensor_T(n):
    """ This function returns the tensor product of n T gates 
    as a numpy 1d-array
    
    Args:
        n (int): number of gates to tensor
    
    Returns:
        (complex) numpy 1d-array of length 2**n
    """
    return np.array([1.0]*(2**n-1)+[np.exp(1j*np.pi/4)])

def multi_RZ(theta,n):
    """ This function returns the C^(n-1)-RZ(theta) on n qubits 
    as a numpy 1d-array 
    
    Args:
        theta (float): angle of the RZ gate
        n (int): number of qubits the gate acts on

    Returns:
        (complex) numpy 1d-array of length 2**n """
    
    return np.array([1.0]*(2**n-1)+[np.exp(1j*theta)])

def multi_CZ(n):
    """ This function returns the C^(n-1)-Z gate on n qubits 
    as a numpy 1d-array 
    
    Args:
        n (int): number of qubits the gate acts on
    
    Returns:
        (float) numpy 1d-array of length 2**n """
    
    out = np.array([1.0]*(2**n-1)+[-1.0])
    if n<=1:
        out = 0
        print("Warning: multi_CZ({}) is not defined".format(n))
    return out

def multi_CS(n):
    """ This function returns the C^(n-1)-S gate on n qubits 
    as a numpy 1d-array
    
    Args:
        n (int): number of qubits the gate acts on
    
    Returns:
        (complex) numpy 1d-array of length 2**n """
    
    out = np.array([1.0]*(2**n-1)+[1j])
    if n<=1:
        out = 0
        print("Warning: multi_CS({}) is not defined".format(n))
    return out

