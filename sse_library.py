import gurobipy as gp
from gurobipy import GRB

def sext_diagonals(A,target,params={'Threads':1, 'FeasibilityTol':1e-9}, only_reals=False, write_to_mps=False):
    """ This function computes the stabilizer extent of a diagonal target state given a matrix A whose columns are the 
    decomposition elements to be used in its optimization. It uses Gurobi as solver, and therefore requires a valid Gurobi license.
    
    Args:
        A: numpy 2d array of shape (2**n, 2**(m+1)) if only_reals=False, else of shape (2**n, 2**m), where n is the number of qubits
            involved and m is the number of elements in the decomposition set. The first half of the columns of A correspond to 
            the real parts of them, say A_r, while the second half to the imaginary parts, say A_i, such that A=numpy.hstack((A_r,A_i));
        target: numpy 1d array of shape (2**n,) corresponding to the target state whose stabilizer extent is to be computed;
        params: dictionary of parameters to be passed to Gurobi. By default we set it to use a single thread to avoid memory overload 
            and maximum feasibility tolerance of 1e-9;
        only_reals: boolean, if True the optimization is performed over real variables only."""
    
    n=int(np.log2(target.shape[0])) # number of qubits

    # number of elements
    if only_reals:
        cardinality=(A.shape[1])
    else:
        cardinality=(A.shape[1]//2)

    # setting the target, as a vector u
    if only_reals:
        u=target
    else:
        u=np.concatenate((np.real(target),np.imag(target)))

    #defining the model
    m=gp.Model() # initialize

    # variables first
    a=m.addMVar(cardinality, lb=-GRB.INFINITY, name="a")
    if not only_reals:
        b=m.addMVar(cardinality, lb=-GRB.INFINITY, name="b")
    t=m.addMVar(cardinality, name="t")

    # now constraints
    if only_reals:
        m.addConstr(a<=t, name="a_leq_t")
        m.addConstr(-t<=a, name="minus_t_leq_a")
        m.addConstr(A@a==u, "A_x_a_equal_u")
    else:
        m.addConstr(a*a+b*b<=t*t, name="a2_plus_b2_leq_t2")
        m.addConstr(A[:,:A.shape[1]//2]@a+(-A[:,A.shape[1]//2:])@b==u[:2**n], name="real_A_x_ab_equal_u")
        m.addConstr(A[:,:A.shape[1]//2]@b+A[:,A.shape[1]//2:]@a==u[2**n:],name="imag_A_x_ab_equal_u")

    # setting the objective
    obj=gp.quicksum(t)
    m.setObjective(obj)

    # setting input parameters
    if len(params) != 0:
        for p in params:
            m.setParam(p, params[p])

    # required step of updating the model before optimization
    m.update()
    if write_to_mps:
        m.write("model.mps") # write the model to a file in mps format

    # optimization itself
    m.optimize()

    # outputs
    if m.status != GRB.OPTIMAL:
        print("Warning: the optimization did not end up with an optimal solution, status code {}".format(m.status))
    else:
        if only_reals:
            out=(m.ObjVal**2, a.X)
        else:
            out=(m.ObjVal**2, a.X+1j*b.X)

    return out

