from itertools import permutations, product

def permutations_matrices(n):
    """ This function returns a list of all n-qubit permutation matrices.
    
    Args:
        n (int): number of qubits the operators act on
    
    Returns:
        list of (int) numpy 2d-arrays of shape (2**n,2**n) """

    bits_permutations = list(permutations(range(1, n+1)))

    out=[]
    for sigma in bits_permutations:
        P = np.zeros((2**n, 2**n), dtype=np.int32)
        # Iterate over all basis states |i_1...i_n⟩
        for bits in product([0, 1], repeat=n):
            original_idx = int(''.join(map(str, bits)), 2)  # Binary to decimal
            permuted_bits = [bits[sigma[k] - 1] for k in range(n)]  # Apply sigma (1-based)
            permuted_idx = int(''.join(map(str, permuted_bits)), 2)
            P[permuted_idx, original_idx] = 1
        out.append(P.copy())
    return out

from numba import jit, prange

@jit(parallel=True, nopython=True)
def matmul_manual(A, B):
    """Manually multiply two int32 matrices A and B.
    
    Args:
        A: numpy 2d array of shape (n, m)
        B: numpy 2d array of shape (m, p)
    
    Returns:
        numpy 2d array of shape (n, p) resulting from A @ B executed "manually". """

    n, m = A.shape
    m2, p = B.shape
    assert m == m2, "Incompatible dimensions for matrix multiplication"
    result = np.zeros((n, p), dtype=np.int32) # Use int32 to avoid overflow
    for i in prange(n):
        for j in prange(p):
            for k in prange(m):
                result[i, j] += int(A[i, k]) * int(B[k, j])
    return result

@jit(parallel=True, nopython=True)
def twirl_permutations(M,n,permutations_elements):
    """ This functions computes the twirling of a matrix M by all n-qubit permutation matrices.
    
    Args:
        M: numpy 2d array of shape (2**n, 2**n)
        n (int): number of qubits the operators act on
        permutations_elements: list of all permutation matrices corresponding to n
    
    Returns:
        numpy 2d array of shape (2**n, 2**n) resulting from the twirling of M by all n-qubit permutation matrices. """

    out = np.zeros((2**n,2**n),dtype=np.int32)
    for i in prange(len(permutations_elements)):
        sigma = permutations_elements[i]
        out = out + matmul_manual(matmul_manual(sigma,M),sigma.T)
    return (1/len(permutations_elements))*out

@jit(parallel=True, nopython=True)
def twirled_A(A,n,permutations_elements):
    """ This functions computes the twirling of all the elements encoded in the columns of A by all n-qubit permutation matrices.
    
    Args:
        A: numpy 2d array of shape (2**n, m)
        n (int): number of qubits the operators act on
        permutations_elements: list of all permutation matrices corresponding to n
        
    Returns:
        numpy 2d array of shape (2**n, m) resulting from the twirling of all the elements encoded in the columns of A."""
    
    out=np.empty(A.shape,dtype=float)
    for i in prange(A.shape[1]):
        out[:,i]=np.diag(twirl_permutations(np.diag(A[:,i]),n,permutations_elements))
    return out

##### auxiliary functions for collecting non-equivalent diagonals by CZ category #####
def Rs_with_phases_from_non_equiv_graphs(adjacency_matrices):
    """ This function returns a list of all resulting symmetric R matrices after including all possible
    phases configurations to all adjacency matrices corresponding to non-equivalent graphs by permutation. 
    For each adjacency matrix, it appends elements with added diagonal parts corresponding to each phase 
    configuration.
    
    Args:
        adjacency_matrices: list of nxn matrices corresponding to non-equivalent graphs as adjacency matrices
        
    Returns:
        list of symmetric R matrices"""

    # number of qubits/vertices
    n=adjacency_matrices[0].shape[0]

    # possible phases
    bs=list(product([0,1], repeat=n))

    # initialize output list
    Rs=[]

    for b in bs:
        for G in adjacency_matrices:
            Rs.append(G+np.diag(b))
    return Rs

@jit(parallel=True, nopython=True)
def remove_equivalent_diagonals(all_diag, permutations_elements, only_reals=False):
    """ This function removes equivalent diagonal Cliffords by some permutation conjugation from a given 
    collection of them. 
    
    Args:
        all_diag: numpy 2d-array whose columns are diagonal Cliffords to be filtered, in the form of numpy.hstack((A_r,A_i)), 
            with A_r the real part of those diagonals and A_i the imaginary one, respectively shifted.
        permutations_elements: list of all permutation matrices corresponding to number of qubits the diagonal Ciffords act on 
        only_reals: boolean, adjust the function in the case of real diagonals only
        
    Returns:
        numpy 2d-array with non-equivalent diagonal Cliffords only, following the same input split between real and imaginary part"""

    mask = np.ones(all_diag.shape[1], dtype=np.bool_)  # Mask to keep track of which diagonals to keep
    if only_reals:
        cardinality=all_diag.shape[1]
    else:
        cardinality=all_diag.shape[1]//2
    for i in range(cardinality):
        if not mask[i]:
            continue
        d_real = all_diag[:,i]
        if not only_reals:
            d_imag = all_diag[:,i + cardinality]
        for p in permutations_elements:
            pdpT_real = np.diag(matmul_manual(matmul_manual(p,np.diag(d_real)),p.T))
            if not only_reals:
                pdpT_imag = np.diag(matmul_manual(matmul_manual(p,np.diag(d_imag)),p.T))
            for j in prange(i + 1, cardinality):
                if not mask[j]:
                    continue
                if only_reals:
                    if (pdpT_real == all_diag[:,j]).all():
                        mask[j] = False  # Mark as duplicate
                else:
                    if (pdpT_real == all_diag[:,j]).all() and (pdpT_imag == all_diag[:,j+cardinality]).all():
                        mask[j] = False  # Mark as duplicate
                        mask[j + cardinality] = False

    # Return only the diagonals that are not marked as duplicates
    return [all_diag[:,i] for i in range(all_diag.shape[1]) if mask[i]]